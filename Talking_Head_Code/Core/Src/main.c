uint16_t find_min_adc(uint16_t *buffer, uint16_t size) {
    uint16_t min_val = buffer[0];  // Initialize with first element
    for (int i = 0; i < size; i++) {
        if (buffer[i] < min_val) {
            min_val = buffer[i];
        }
    }
    return min_val;
}

uint16_t find_max_adc(uint16_t *buffer, uint16_t size) {
    uint16_t max_val = buffer[0];  // Initialize with first element
    for (int i = 0; i < size; i++) {
        if (buffer[i] > max_val) {
            max_val = buffer[i];
        }
    }
    return max_val;
}

void transmitCoordinates(float x, float y) {
  // Format the coordinates as a string
  int len = sprintf(txBuffer, "%.2f,%.2f\r\n", x, y);

  // Transmit the data
  HAL_UART_Transmit(&huart5, (uint8_t*)txBuffer, len, 100);
}

void heat_focus()	{

	// FROM OPEN SOURCE ARDUINO CODE

    float x = 0;
    float y = 0;
    float total = 0.0;

    float minVal = 100, maxVal = 0;
    int i = 0;
    for (float yPos = 3.5; yPos > -4; yPos -= 1.0) {
        for (float xPos = 3.5; xPos > -4; xPos -= 1.0) {

        	uint16_t val = heat_pixels[i];
        	uint16_t absVal = (val & 0x7FF);
            float p = (val & 0x800) ? 0 - (float)absVal : (float)absVal;
            total += (float)val;
            p *= .25;
            x += xPos * p;
            y += yPos * p;
            minVal = heat_min(minVal, p);
            maxVal = heat_max(maxVal, p);
            i++;
        }
    }
    heat_avg = total / 64.0;
    heat_max_val = maxVal;
    x = (- x / 64.0) / 5.0;
    y = (y / 64.0) / 5.0;
    heat_y = heat_max(-1.0, heat_min(1.0, x));
    heat_x = heat_max(-1.0, heat_min(1.0, y));

}

int motor_pending_steps = 0;
int motor_next_task = 0;
int motor_interrupt_task = 0;
extern const unsigned int MOTOR_SLOWING_DOWN_STEP; // the absolute value of step that starts to slow down

void move_head(int steps)	{ // negative left, pos right
	/*
	 * This handles the case where it's already moving and you're 'updating' the movement, makes it bounce so it looks fluid
	 *    cases:
	 *      1. Not moving - just changes pending (and resets next_task)
	 *      2. Moving in same direction - just changes pending (and resets next_task)
	 *      4. Currently moving in opposite direction - changing pending just enough so it can 'bounce' and updates steps to next_task
	 */

	motor_interrupt_task = steps;

    HAL_TIM_Base_Stop_IT(&htim16); // so interrupt is never triggered
	if(motor_pending_steps == 0)	{ // if there's a next task it gets overwritten
		motor_pending_steps = steps;
		motor_next_task = 0;
	}
	HAL_TIM_Base_Start_IT(&htim16);
}


